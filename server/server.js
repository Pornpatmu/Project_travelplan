app.get('/places/random', (req, res) => {  
  const province = req.query.province?.trim();
  const rawCategory = req.query.category?.trim(); // <- รองรับหลายค่า
  const companion = req.query.companion?.trim()?.toLowerCase();
  const tripType = req.query.tripType?.trim()?.toLowerCase();

  console.log(`[REQ] province='${province}' | category='${rawCategory}' | companion='${companion}' | tripType='${tripType}'`);

  if (!province || !rawCategory) {
    return res.status(400).json({ error: 'Missing province or category' });
  }

  const categories = rawCategory.split(',').map(c => c.trim().toLowerCase()); // แยกเป็น array
  let query = `
    SELECT * FROM all_places 
    WHERE TRIM(LOWER(province)) = TRIM(LOWER(?))
      AND TRIM(LOWER(type)) != 'accommodation'
  `;
  const params = [province];

  // สร้างเงื่อนไข category แบบหลายค่า
  if (categories.length > 0) {
    const categoryConditions = categories.map(() => `LOWER(category) LIKE ?`);
    query += ` AND (${categoryConditions.join(' OR ')})`;
    categories.forEach(c => params.push(`%${c}%`));
  }

  // เงื่อนไข tags: companion / tripType
  const tagConditions = [];
  if (companion) {
    tagConditions.push(`LOWER(tags) LIKE ?`);
    params.push(`%${companion}%`);
  }
  if (tripType && !categories.includes(tripType)) {
    tagConditions.push(`LOWER(tags) LIKE ?`);
    params.push(`%${tripType}%`);
  }

  if (tagConditions.length > 0) {
    query += ` AND (${tagConditions.join(' OR ')})`;
  }

  query += ` ORDER BY RANDOM() LIMIT 5`;

  db.all(query, params, (err, rows) => {
    if (err) return res.status(500).json({ error: err.message });
    if (rows.length === 0) return res.status(404).json({ error: 'ไม่พบสถานที่' });
    res.json(rows);
  });
});





