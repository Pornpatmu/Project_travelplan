const express = require('express');
const sqlite3 = require('sqlite3').verbose();
const cors = require('cors');

const app = express();
const port = 3000;

app.use(cors());
app.use(express.json());

// --- เปิด database ---
const db = new sqlite3.Database('./mydatabase.db');

// --- สร้างตารางต่างๆ 
db.serialize(() => {
  // ตารางแผนเที่ยว
  db.run(`
    CREATE TABLE IF NOT EXISTS plans (
  id INTEGER PRIMARY KEY AUTOINCREMENT,
  name TEXT UNIQUE,  
  province TEXT,
  start_date TEXT,
  end_date TEXT,
  budget REAL,
  spending REAL,
  dayColors TEXT
)

  `);

  db.run(`
    CREATE TABLE IF NOT EXISTS places (
      id INTEGER PRIMARY KEY AUTOINCREMENT,
      plan_id INTEGER,
      day_index INTEGER,
      place_id INTEGER,
      expense REAL,
      order_index INTEGER,
      category TEXT
    )
  `);

  db.run(`
    CREATE TABLE IF NOT EXISTS favorite_places (
      id INTEGER PRIMARY KEY AUTOINCREMENT,
      plan_id INTEGER,
      name TEXT,
      lat REAL,
      lon REAL,
      category TEXT
    )
  `);

  db.run(`
    CREATE TABLE IF NOT EXISTS other_expenses (
      id INTEGER PRIMARY KEY AUTOINCREMENT,
      plan_id INTEGER,
      desc TEXT,
      amount REAL,
      icon_code INTEGER
    )
  `);
});

db.run(`
  CREATE TABLE IF NOT EXISTS all_places (
    id INTEGER PRIMARY KEY AUTOINCREMENT,
    name TEXT,
    lat REAL,
    lon REAL,
    category TEXT,
    type TEXT,
    province TEXT,
    image TEXT,
    openingDays TEXT,
    phone TEXT,
    description TEXT,
    address TEXT,
    hours TEXT,
    mapUrl TEXT,
    tags TEXT
  )
`);


